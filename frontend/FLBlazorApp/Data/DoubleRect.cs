namespace FLBlazorApp.Data
{
    public class DoubleRect
    {
        public double X;

        public double Y;

        public double Width;

        public double Height;
        public DoubleRect(double x, double y, double width, double height)
        {
            X = x;
            Y = y;
            Width = width;
            Height = height;
        }
    }
}
